package UI;

import java.awt.*;

public class Description {
    String description= "";
    Color rgb= Color.BLACK;

    public Description(){
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Color getRgb() {
        return rgb;
    }

    public void setRgb(Color rgb) {
        this.rgb = rgb;
    }
}
